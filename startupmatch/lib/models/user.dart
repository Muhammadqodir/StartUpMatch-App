class User {
  int id;
  String name;
  String username;
  String email;
  String usertype;
  String pic;
  String joined;
  String location;
  User({
    required this.id,
    required this.name,
    required this.username,
    required this.email,
    required this.usertype,
    required this.pic,
    required this.joined,
    required this.location,
  });
}
